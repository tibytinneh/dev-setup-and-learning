#include "libft.h"

/*
**	`ft_isalpha` checks for alphabetic character
**	Either within 'A'(65) to 'Z'(90) or 'a'(97) to 'z'(122)
*/

int	ft_isalpha(int c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
}
