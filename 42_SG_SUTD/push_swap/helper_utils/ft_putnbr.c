#include "../push_swap.h"

static void	recur(int n)
{
	if (n >= 10 || n <= -10)
		recur(n / 10);
	n %= 10;
	if (n < 0)
		n = -n;
	write(1, &"0123456789"[n], 1);
}

void	ft_putnbr(int n)
{
	if (n < 0)
		write(1, "-", 1);
	recur(n);
}
